    private void aumentarQTDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aumentarQTDActionPerformed
        // AUMENTAR QTD
        int linha_selecionada = tabelaCarrinho.getSelectedRow();
        
        if(linha_selecionada!=-1){
            String nomeProduto = tabelaCarrinho.getValueAt(linha_selecionada, 0).toString();
            String valorProduto = tabelaCarrinho.getValueAt(linha_selecionada, 1).toString();
            String descricaoProduto = tabelaCarrinho.getValueAt(linha_selecionada, 2).toString();
            String qtdProduto = tabelaCarrinho.getValueAt(linha_selecionada, 3).toString();
            String valorTotal = tabelaCarrinho.getValueAt(linha_selecionada, 4).toString();
            
            String qtdNovo = qtdNew.getText().trim();
            
            if(Integer.parseInt(qtdNovo)>0 && !qtdNovo.equals("")){
                Connection con = DBConnection.getConnection();
                try{
                    String emailUser = "select idUsuario from tb_usuario where emailUsuario = ?";
                    PreparedStatement pstEmail = con.prepareStatement(emailUser);
                    pstEmail.setString(1, emailPessoal);
                    ResultSet rs = pstEmail.executeQuery();

                    if (rs.next()){
                        int id = rs.getInt("idUsuario");

                        try{
                            String sql = "select * from tb_carrinho where nomeProduto=? AND valorProduto=? AND descricaoProduto=? AND qtdProduto=? AND valorTotal=? AND fkUsuario=?";
                            PreparedStatement pst = con.prepareStatement(sql);
                            pst.setString(1, nomeProduto);
                            pst.setDouble(2, Double.parseDouble(valorProduto));
                            pst.setString(3, descricaoProduto);
                            pst.setInt(4, Integer.parseInt(qtdProduto));
                            pst.setDouble(5, Double.parseDouble(valorTotal));
                            pst.setInt(6, id);
                            ResultSet result = pst.executeQuery();

                            if(result.next()){
                                int idProduto = result.getInt("idProduto");
                                
                                String update = "update tb_carrinho set qtdProduto=?, valorTotal=? where idProduto=?";
                                PreparedStatement pstUpdate = con.prepareStatement(update);
                                pstUpdate.setInt(1,(Integer.parseInt(qtdProduto))+Integer.parseInt(qtdNovo));
                                pstUpdate.setDouble(2,(Double.parseDouble(valorProduto))*((Integer.parseInt(qtdProduto))+Integer.parseInt(qtdNovo)));
                                pstUpdate.setInt(3, idProduto);
                                pstUpdate.executeUpdate();
                                atualizandoTabelaCarrinho();   

                            }else{
                                    JOptionPane.showMessageDialog(this, "Algo deu errado, tente novamente!");
                            }                    
                        }catch(Exception e){
                            e.printStackTrace();
                        }
                    }
                }catch(Exception e){
                    e.printStackTrace();
                }
            }else{
             JOptionPane.showMessageDialog(this, "Selecione uma quantidade maior que 0!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Parece que você não tem nada selecionado!");
        }
    }//GEN-LAST:event_aumentarQTDActionPerformed

