    public void atualizandoTabelaCarrinho(){
        DefaultTableModel model = (DefaultTableModel)tabelaCarrinho.getModel();
        valorCompra = 0;
            // APAGANDO O QUE JÁ EXISTE
            int i = tabelaCarrinho.getRowCount();
            for (int r=0;r<i;r++){
                model.removeRow(0);
            }
            
            Connection con = DBConnection.getConnection(); 
            
            // COMPARANDO EMAIL
            try{
                String emailUser = "select idUsuario from tb_usuario where emailUsuario = ?";
                PreparedStatement pstEmail = con.prepareStatement(emailUser);
                pstEmail.setString(1, emailPessoal);
                ResultSet rs = pstEmail.executeQuery();
                
                if (rs.next()){
                    int id = rs.getInt("idUsuario");
                    
                    // COMPARANDO ID
                    try{
                        String sql = "select * from tb_carrinho where fkUsuario = ?";                 
                        PreparedStatement pst = con.prepareStatement(sql);
                        pst.setInt(1, id);
                        ResultSet rs2 = pst.executeQuery();
                        
                        while(rs2.next()){
                            String numero = rs2.getString("nomeProduto");
                            double valor = rs2.getDouble("valorProduto");
                            String desc = rs2.getString("descricaoProduto");
                            int qtd = rs2.getInt("qtdProduto");
                            double valorTotal = rs2.getDouble("valorTotal");
                            valorCompra += valorTotal;

                            model.addRow(new Object[]{numero,valor,desc,qtd,valorTotal});
                        }
                        total.setText("Valor da compra: " + (Math.round(valorCompra*100.0)/100.0));
                        totall.setText("Valor da compra: " + (Math.round(valorCompra*100.0)/100.0));
                    }catch(Exception e){
                        e.printStackTrace();
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Algo deu errado");
                }
                
            }catch(Exception e){
                e.printStackTrace();
            }
    }
    
